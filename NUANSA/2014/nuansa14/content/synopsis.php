		<hr class="belowtitle">
		<p>As Lantip leaves his home village Wanagalih, so turns the page to a new chapter of his life.</p>
		<p>Lantip bids his dearest mother farewell as he is adopted into the esteemed Priyayi family of Sastrodarsono, where he learns the ways of the Priyayi under the stern tutelage of Sastrodarsono’s son, Noegroho.</p>
		<p>Having grown into a young man with a vehement passion for the arts, Lantip has to choose between the life Noegroho has planned out for him and one that he truly aspires to lead. At the same time, he comes to learn the hard fact that not every Priyayi can live up to the Priyayi ideals he has in mind.</p>
		<p>Inspired by Umar Kayam’s Para Priyayi, NUANSA will present the multifarious beauty of the Javanese culture as Lantip endeavours through the complexities of the Priyayi life as he seeks to learn what it really means to be a Priyayi.</p>
